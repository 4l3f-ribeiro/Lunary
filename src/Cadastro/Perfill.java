    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
         classUsuario usuarioLogado = classUsuario.Session.getUsuarioLogado();
        usuarioLogado.setId(Integer.parseInt(txtID.getText()));
        
        conUsuario usu = new conUsuario();
        
        try {
            usu.excluir(usuarioLogado);
            JOptionPane.showMessageDialog(null,"Usuario Excluido com Sucesso");
            telalogin login = new telalogin();
            login.setVisible(true);
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(Perfill.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


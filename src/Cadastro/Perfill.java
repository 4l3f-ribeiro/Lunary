    private void meuperfil(){
        classUsuario usuarioLogado = classUsuario.Session.getUsuarioLogado();

        String nomeUsuario = usuarioLogado.getNome();
        String email = usuarioLogado.getEmail();

        int tempo = usuarioLogado.getTempo();
        int id = usuarioLogado.getId();

        int acerto = usuarioLogado.getAcerto();
        int erro = usuarioLogado.getErro();
        txtNome.setText(nomeUsuario);
        txtEmail.setText(email);
        txtID.setText(String.valueOf(id));
        txtTempo.setText(String.valueOf(tempo));
        txtAcertos.setText(String.valueOf(acerto));
        txtErros.setText(String.valueOf(erro));
}
    


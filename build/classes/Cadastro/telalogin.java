    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
try {
        String email, senha;
        classUsuario objLogin = new classUsuario();
        
        email = txtNome.getText(); // Coleta o email do campo de texto
        senha = txtSenha.getText(); // Coleta a senha do campo de texto
        
        objLogin.setEmail(email);
        objLogin.setSenha(senha);

        conUsuario objusuario = new conUsuario();
        
        // Chama o método de login, que irá verificar e armazenar os dados no objLogin
        if (objusuario.Login(objLogin)) {
           
             Session.setUsuarioLogado(objLogin);
            
            // Aqui você pode mudar para a tela principal
            PrincipalPage objmudarsenha = new PrincipalPage();
            
            objmudarsenha.setVisible(true);
           
            dispose(); // Fecha a tela de login
        } else {
            JOptionPane.showMessageDialog(null, "Email ou senha inválidos.");
        }
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(null, "Erro: " + erro.getMessage());
    }

  
       
        
    }//GEN-LAST:event_entrarActionPerformed

